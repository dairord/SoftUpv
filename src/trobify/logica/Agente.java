public class Agente {
    String username, agencia;

    public Agente(String username, String agencia) {
        this.username = username;
        this.agencia = agencia;
    }

    public String getUsername() {
        return username;
    }

    public String getAgencia() {
        return agencia;
    }
    
    
}
